    public void listarProductos(){
        List<Producto> listadoProductos = controladorProducto.obtenerProductos();
        modeloProducto = (DefaultTableModel) tablaProductos.getModel();
        modeloProducto.setRowCount(0);
        for (int i = 0;i < listadoProductos.size();i++){
            Producto p = listadoProductos.get(i);
            Object [] fila = {
                p.getCodigoProducto(),
                p.getDescripcion(),
                p.getProveedor(),
                p.getStock()
            };
            modeloProducto.addRow(fila);
        }
    }
    
    private void listarDetalles(){
        List<DetalleVenta> listadoDetalles = controladorDetalle.obtenerDetalleVentas(codigoVenta);
        modelo = (DefaultTableModel) tablaDetalles.getModel();
        modelo.setRowCount(0);
        setTotalCero();
        for (int i = 0;i < listadoDetalles.size();i++){
            actualizarTotal(listadoDetalles.get(i).getSubtotal());
            DetalleVenta dv = listadoDetalles.get(i);
            Object[] fila = {
                dv.getCodigoDetalle(),
                dv.getCodigoProducto(),
                dv.getDescripcionProducto(),
                dv.getPrecio(),
                dv.getCantidad(),
                dv.getSubtotal()
            };
            modelo.addRow(fila);
        }
        txtTotal.setText("$" + String.valueOf(getTotal()));
    }
