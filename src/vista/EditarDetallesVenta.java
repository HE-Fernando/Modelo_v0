    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        DetalleVenta dv = new DetalleVenta();
        dv.setCodigoVenta(codigoVenta);
        dv.setCodigoProducto(Integer.parseInt(txtIdProducto.getText()));
        dv.setCantidad(Integer.parseInt(txtCantidad.getText()));
        dv.setPrecio(Double.parseDouble(txtPrecio.getText()));
        dv.setSubtotal();
        if (controladorDetalle.registrar(dv)){
            JOptionPane.showMessageDialog(this, "¡Detalle registrado exitosamente!");
            listarDetalles();
            limpiarCampos();
        } else{
            JOptionPane.showMessageDialog(this, "¡Error al registrar detalle!");
        }
        listarDetalles();
        listarProductos();
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        // TODO add your handling code here:
        if (txtIdDetalle.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione un detalle de la tabla");
            return;
        }
        int codigoDetalle = Integer.parseInt(txtIdDetalle.getText());
        int cantidadActualizar = Integer.parseInt(txtCantidad.getText());
        double precio = Double.parseDouble(txtPrecio.getText());
        int codigoProducto = Integer.parseInt(txtIdProducto.getText());
        boolean restar;
        if (cantidadActualizar < getCantidadActual()){
            restar = false;
        } else{
            restar = true;
            setCantidadStock(cantidadActualizar - getCantidadActual());
        }
        if (controladorDetalle.actualizar(codigoDetalle, cantidadActualizar, precio, codigoProducto, restar, getCantidadStock())) {
            JOptionPane.showMessageDialog(this, "¡Detalle actualizado!");
            listarDetalles();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "¡Error al actualizar detalle!");
        }
        listarProductos();
        listarDetalles();
    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:
        if (txtIdDetalle.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione un detalle para eliminar");
            return;
        }
        int codigoDetalle = Integer.parseInt(txtIdDetalle.getText());
        int confirm = JOptionPane.showConfirmDialog(this, "¿Eliminar detalle?");
        if (confirm == JOptionPane.YES_OPTION){
            if (controladorDetalle.eliminar(codigoDetalle)){
                JOptionPane.showMessageDialog(this, "Detalle eliminado");
                listarDetalles();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar detalle");
            }
        }
        listarDetalles();
        listarProductos();
    }//GEN-LAST:event_botonEliminarActionPerformed

