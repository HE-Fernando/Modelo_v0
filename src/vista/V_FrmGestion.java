    Cliente_UsuarioDao controladorCliente = new Cliente_UsuarioDao();
    DefaultTableModel modeloClientes = new DefaultTableModel();
    public void listarClientes(){
        List<Cliente> listadoClientes = controladorCliente.obtenerClientes();
        modeloClientes = (DefaultTableModel) tablaClientes.getModel();
        modeloClientes.setRowCount(0);
        for (int i = 0;i < listadoClientes.size();i++){
            Cliente c = listadoClientes.get(i);
            Object[] fila = {
                c.getCodigoCliente(),
                c.getNombreCliente(),
                c.getDireccion(),
                c.getTelefono()
            };
            modeloClientes.addRow(fila);
        }
    }
    //RELLENADO DE TABLA
    VentaDao controladorVenta = new VentaDao();
    DetalleVentaDao controladorDetalles = new DetalleVentaDao();
    DefaultTableModel modelo = new DefaultTableModel();
    public void listarVentas(){
        List<Venta> listadoVentas = controladorVenta.obtenerVentas();
        modelo = (DefaultTableModel) tablaVentas.getModel();
        modelo.setRowCount(0);
        for (int i = 0;i < listadoVentas.size();i++){
            double auxiliar = 0;
            List<DetalleVenta> listadoDetalles = controladorDetalles.obtenerDetalleVentas(listadoVentas.get(i).getCodigoVenta());
            for (int k = 0;k < listadoDetalles.size();k++){
                auxiliar = auxiliar + listadoDetalles.get(k).getSubtotal();
            }
            Venta v = listadoVentas.get(i);
            Object [] fila = {
                v.getCodigoVenta(),
                v.getCodigoCliente(),
                v.getFecha(),
                "$" + auxiliar
            };
            controladorVenta.actualizarTotal(listadoVentas.get(i).getCodigoVenta(), auxiliar);
            modelo.addRow(fila);
        }
    }
