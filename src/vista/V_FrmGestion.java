    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        Venta v = new Venta();
        v.setCodigoCliente(Integer.parseInt(txtIdCliente.getText()));
        v.setFecha(LocalDate.parse(txtFecha.getText()));
        if (controladorVenta.registrar(v)){
            JOptionPane.showMessageDialog(this, "¡Venta registrada exitosamente!");
            listarVentas();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "¡Error al registrar venta!");
        }
        listarClientes();
        listarVentas();
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        // TODO add your handling code here:
        if (txtIdVenta.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione una venta de la tabla");
            return;
        }
        int codigoVenta = Integer.parseInt(txtIdVenta.getText());
        EditarDetallesVenta ventanaDetalles = new EditarDetallesVenta(codigoVenta);
        //listener para cuando se cierre
        ventanaDetalles.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                listarClientes();
                listarVentas();
            }
        });
        ventanaDetalles.setVisible(true);
        ventanaDetalles.setLocationRelativeTo(this);
    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:
        if (txtIdVenta.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione una venta para eliminar");
            return;
        }
        int id = Integer.parseInt(txtIdVenta.getText());
        int confirm = JOptionPane.showConfirmDialog(this, "¿Eliminar venta?");
        if (confirm == JOptionPane.YES_OPTION){
            if (controladorVenta.eliminar(id)){
                JOptionPane.showMessageDialog(this, "Venta eliminada");
                listarVentas();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar venta");
            }
        }
        listarClientes();
        listarVentas();
    }//GEN-LAST:event_botonEliminarActionPerformed

