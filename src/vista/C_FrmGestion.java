    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        Cliente c = new Cliente();
        c.setNombreCliente(txtNombre.getText());
        c.setDireccion(txtDireccion.getText());
        c.setTelefono(txtTelefono.getText());
        c.setUsuario(txtUsuario.getText());
        c.setContraseña(txtContraseña.getText());
        if (controladorCliente.registrar(c)){
            JOptionPane.showMessageDialog(this, "¡Cliente registrada correctamente!");
            listarClientes();
            limpiarCampos();
        } else{
            JOptionPane.showMessageDialog(this, "¡Error al registrar cliente!");
        }
    }//GEN-LAST:event_botonAgregarActionPerformed

    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione un cliente de la tabla");
            return;
        }
        int codigoCliente = Integer.parseInt(txtId.getText());
        String nombre = txtNombre.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        if (controladorCliente.actualizar(codigoCliente, nombre, direccion, telefono)) {
            JOptionPane.showMessageDialog(this, "¡Cliente actualizado!");
            listarClientes();
            limpiarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "¡Error al actualizar cliente!");
        }
        
    }//GEN-LAST:event_botonEditarActionPerformed

