    private void tablaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClientesMouseClicked
        // TODO add your handling code here:
        int fila = tablaClientes.rowAtPoint(evt.getPoint());
        txtId.setText(tablaClientes.getValueAt(fila, 0).toString());
        txtNombre.setText(tablaClientes.getValueAt(fila, 1).toString());
        txtDireccion.setText(tablaClientes.getValueAt(fila, 2).toString());
        txtTelefono.setText(tablaClientes.getValueAt(fila, 3).toString());
    }//GEN-LAST:event_tablaClientesMouseClicked

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        // TODO add your handling code here:
        if (txtId.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Seleccione un cliente para eliminar");
            return;
        }
        int id = Integer.parseInt(txtId.getText());
        int confirm = JOptionPane.showConfirmDialog(this, "¿Eliminar cliente?");
        if (confirm == JOptionPane.YES_OPTION){
            if (controladorCliente.eliminar(id)){
                JOptionPane.showMessageDialog(this, "Cliente elimnado");
                listarClientes();
                limpiarCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Error al eliminar cliente");
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

