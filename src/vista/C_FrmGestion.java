    public void listarClientes(){
        List<Cliente> listadoClientes = controladorCliente.obtenerClientes();
        modelo = (DefaultTableModel) tablaClientes.getModel();
        modelo.setRowCount(0);
        for (int i = 0;i < listadoClientes.size();i++){
            Cliente c = listadoClientes.get(i);
            Object[] fila = {
                c.getCodigoCliente(),
                c.getNombreCliente(),
                c.getDireccion(),
                c.getTelefono()
            };
            modelo.addRow(fila);
        }
    }
